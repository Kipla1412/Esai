def add_numbers(a: int,b:int) ->int:
    """
    Returns add  two numbers

    Args:

       a: first number
       b:second number

    Returns:
      the sum of a and b
    
    """
    return a+b

def multiply(a:int,b:int) ->int:
    """
    Returns multiply two numbers

    Args:

       a: first number
       b:second number

    Returns:
      the product of a and b
    
    """
    return a*b
